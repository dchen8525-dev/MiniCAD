    @Test
    void shouldExportStlBinary() {
        byte[] stl = StepMeshExporter.exportStlBinary(MINIMAL_SQUARE);

        // STL binary: 80-byte header + 4-byte triangle count + 50 bytes per triangle
        assertTrue(stl.length > 84, "STL binary should be larger than header");

        // Check triangle count
        int triangleCount = ((stl[83] & 0xFF) << 24) | ((stl[82] & 0xFF) << 16)
                | ((stl[81] & 0xFF) << 8) | (stl[80] & 0xFF);
        assertTrue(triangleCount >= 1, "Should have at least 1 triangle, got " + triangleCount);

        // Verify size: 80 + 4 + (triangles * 50)
        int expectedSize = 80 + 4 + (triangleCount * 50);
        assertEquals(expectedSize, stl.length, "STL binary size should match expected");
    }

