    @Test
    void shouldExportObj() {
        String obj = StepMeshExporter.exportObj(MINIMAL_SQUARE);

        assertTrue(obj.contains("# Generated by MiniCAD"));
        assertTrue(obj.contains("v "));
        assertTrue(obj.contains("vn "));
        assertTrue(obj.contains("f "));

        // Count vertices, normals, faces
        long vCount = obj.lines().filter(l -> l.startsWith("v ")).count();
        long vnCount = obj.lines().filter(l -> l.startsWith("vn ")).count();
        long fCount = obj.lines().filter(l -> l.startsWith("f ")).count();

        assertTrue(vCount >= 3, "Should have at least 3 vertices, got " + vCount);
        assertTrue(vnCount >= 1, "Should have at least 1 normal, got " + vnCount);
        assertTrue(fCount >= 1, "Should have at least 1 face, got " + fCount);
    }

    @Test
    void shouldExportSameObjFromCompiledDocument() {
        CompiledStepDocument compiled = CompiledStepDocument.compile(MINIMAL_SQUARE);

        String direct = StepMeshExporter.exportObj(MINIMAL_SQUARE);
        String fromCompiled = StepMeshExporter.exportObj(compiled);

        assertEquals(direct, fromCompiled);
    }

